a = "100"

print(int(a,2))     # Binary
print(int(a,8))     # Octal
print(int(a,16))    # Hexadecimal

a = 100

print(bin(a))       # Decimal to Binary
print(oct(a))       # Decimal to Octal
print(hex(a))       # Decimal to Hexadecimal

a = 100

print(bin(a)[2:])   # After Slicing
print(oct(a)[2:])   # After Slicing
print(hex(a)[2:])   # After Slicing